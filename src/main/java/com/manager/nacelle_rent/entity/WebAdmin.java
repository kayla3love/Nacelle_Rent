package com.manager.nacelle_rent.entity;

public class WebAdmin {
    private String webAdminId;
    private String webAdminPassword;
    private int webAdminFlag;

    public String getWebAdminId() {
        return webAdminId;
    }

    public void setWebAdminId(String webAdminId) {
        this.webAdminId = webAdminId;
    }

    public String getWebAdminPassword() {
        return webAdminPassword;
    }

    public void setWebAdminPassword(String webAdminPassword) {
        this.webAdminPassword = webAdminPassword;
    }

    public int getWebAdminFlag() {
        return webAdminFlag;
    }

    public void setWebAdminFlag(int webAdminFlag) {
        this.webAdminFlag = webAdminFlag;
    }
}
